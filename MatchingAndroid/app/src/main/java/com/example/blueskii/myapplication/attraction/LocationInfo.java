package com.example.blueskii.myapplication.attraction;

public class LocationInfo {
    private int bminor;
    private int lid;
    private String lname;

    public int getBminor() {
        return bminor;
    }

    public void setBminor(int bminor) {
        this.bminor = bminor;
    }

    public int getLid() {
        return lid;
    }

    public void setLid(int lid) {
        this.lid = lid;
    }

    public String getLname() {
        return lname;
    }

    public void setLname(String lname) {
        this.lname = lname;
    }
}


